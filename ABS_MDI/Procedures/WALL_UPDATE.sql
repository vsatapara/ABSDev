CREATE OR REPLACE PROCEDURE abs_mdi."WALL_UPDATE" 
(
    WallId IN NUMBER,
    BuildingInformationId IN NUMBER,
    Type IN NUMBER,
    Gauge IN NUMBER,
    Thick IN NUMBER,
    BaseFraming IN NUMBER,
    BaseFlash IN NUMBER,
    BaseClosure IN NUMBER,
    SealedWall IN CHAR,
    Washers IN CHAR,
    Notch IN CHAR,
    RakeClosure IN CHAR,
    EaveClosureStrip IN CHAR,
    FastenerType IN NUMBER,
    FastenerHeadFinish IN NUMBER,
    FastenerLength IN NUMBER,
    Warranty IN CHAR,
    FactoryAppliedSealant IN NUMBER,
    ReverseRolled IN CHAR,
    OutsideMetalClosure IN CHAR,
    SidelapSealant IN CHAR,
    FoamTape IN CHAR,
    ColdStorageBuilding IN CHAR,
    ExteriorSkinProfile IN NUMBER,
    ExteriorSkinTexture IN NUMBER,
    InteriorSkinProfile IN NUMBER,
    InteriorSkinTexture IN NUMBER,
    InteriorSkinGauge IN NUMBER,
    Width IN NUMBER,
    NotchWidth IN NUMBER,
    NotchDepth IN NUMBER,
    NotchType IN NUMBER,
    RValue IN NUMBER,
    UValue IN NUMBER,
    UserId IN NUMBER,
    IpAddress IN VARCHAR2 DEFAULT NULL
)
AS 
BEGIN
    UPDATE "Input_Wall"
    SET
        "BuildingInformationId"=BuildingInformationId,
        "Type"=Type,
        "Gauge"=case when Gauge=0 then null else Gauge end,
        "Thick"=case when Thick=0 then null else Thick end,
        "BaseFraming"=case when BaseFraming=0 then null else BaseFraming end,
        "BaseFlash"=case when BaseFlash=0 then null else BaseFlash end,
        "BaseClosure"=case when BaseClosure=0 then null else BaseClosure end,
        "SealedWall"=SealedWall,
        "Washers"=Washers,
        "Notch"=Notch,
        "RakeClosure"=RakeClosure,
        "EaveClosureStrip"=EaveClosureStrip,
        "FastenerType"=case when FastenerType=0 then null else FastenerType end,
        "FastenerHeadFinish"=case when FastenerHeadFinish=0 then null else FastenerHeadFinish end,
        "FastenerLength"=case when FastenerLength=0 then null else FastenerLength end,
        "Warranty"=Warranty,
        "FactoryAppliedSealant"=case when FactoryAppliedSealant=0 then null else FactoryAppliedSealant end,
        "ReverseRolled"=ReverseRolled,
        "OutsideMetalClosure"=OutsideMetalClosure,
        "SidelapSealant"=SidelapSealant,
        "FoamTape"=FoamTape,
        "ColdStorageBuilding"=ColdStorageBuilding,
        "ExteriorSkinProfile"=case when ExteriorSkinProfile=0 then null else ExteriorSkinProfile end,
        "ExteriorSkinTexture"=case when ExteriorSkinTexture=0 then null else ExteriorSkinTexture end,
        "InteriorSkinProfile"=case when InteriorSkinProfile=0 then null else InteriorSkinProfile end,
        "InteriorSkinTexture"=case when InteriorSkinTexture=0 then null else InteriorSkinTexture end,
        "InteriorSkinGauge"=case when InteriorSkinGauge=0 then null else InteriorSkinGauge end,
        "Width"=case when Width=0 then null else Width end,
        "NotchWidth"=NotchWidth,
        "NotchDepth"=NotchDepth,
        "NotchType"=NotchType,
        "RValue"=RValue,
        "UValue"=UValue,
        "ModifiedBy"=UserId,
        "ModifiedDate"=CURRENT_TIMESTAMP,
        "IpAddress"=IpAddress
    WHERE "WallId" = WallId;
END Wall_Update;
/